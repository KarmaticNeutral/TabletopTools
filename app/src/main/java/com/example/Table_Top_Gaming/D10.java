package com.example.Table_Top_Gaming;

/**
 * This class creates a Dice object with 10 sides and a name of "d10"
 */
public class D10 extends Die{

    /**
     * This default constructor sets the default values of a 10 sided dice
     */
    public D10() {
        setName("d10");
        setNumSides(10);
    }
}
