package com.example.Table_Top_Gaming;


/**
 * This class creates a Dice object with 20 sides and a name of "d20"
 */
public class D20 extends Die{

    /**
     * This default constructor sets the default values of a 20 sided dice
     */
    public D20() {
        setName("d20");
        setNumSides(20);
    }
}
