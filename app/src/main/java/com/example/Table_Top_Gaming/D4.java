package com.example.Table_Top_Gaming;

/**
 * This class creates a Dice object with 4 sides and a name of "d4"
 */
public class D4 extends Die{

    /**
     * This default constructor sets the default values of a 4 sided dice
     */
    public D4() {
        setName("d4");
        setNumSides(4);
    }
}
