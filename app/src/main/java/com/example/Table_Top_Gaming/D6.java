package com.example.Table_Top_Gaming;

/**
 * This class creates a Dice object with 6 sides and a name of "d6"
 */
public class D6 extends Die{

    /**
     * This default constructor sets the default values of a 6 sided dice
     */
    public D6() {
        setName("d6");
        setNumSides(6);
    }
}
