package com.example.Table_Top_Gaming;

/**
 * This class creates a Dice object with 8 sides and a name of "d8"
 */
public class D8 extends Die{
    public D8() {

        /**
         * This default constructor sets the default values of a 8 sided dice
         */
        setName("d8");
        setNumSides(8);
    }
}
